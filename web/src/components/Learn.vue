    <section id="legal">
      <h3>The legal path</h3>
      <ul>
        <li><b>Your own ground</b> (garden, courtyard, own roof):
          mostly yours to change. Utilities first — every EU country
          runs a free call-before-you-dig service (KLIC NL, DICT FR,
          Leitungsauskunft DE…). Heritage rules can restrict visible
          changes; shared courtyards need the association's written
          yes.</li>
        <li><b>Rented:</b> the owner decides — written consent
          first. A planter needs nobody's permission.</li>
        <li><b>Public or institutional</b> — most of the board:
          never DIY; prying a public tile is criminal damage in most
          jurisdictions. Lawful levers instead: façade-garden
          schemes, tree-pit adoption, official depave programs,
          school greening grants, participatory budgets — and the
          block petition, always legal, everywhere.</li>
      </ul>
      <p class="muted">
        Nothing here asks for an illegal act: every square has a
        lawful move. Rules are municipal and change — your city hall
        is the authority. Education, not legal advice.
      </p>
