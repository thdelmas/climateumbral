    <section id="why">
      <h3>Why cities cook at night</h3>
      <ul>
        <li><b>Day:</b> soil and plants spend the sun's energy
          evaporating water; dry dark surfaces bank it as heat.</li>
        <li><b>Night:</b> the bank pays out — asphalt and concrete
          radiate the day's sun back until dawn. A park cools within
          an hour of sunset; a parking lot is warm at 3 a.m.</li>
        <li><b>Who it kills:</b> heatwave deaths track hot nights,
          when sleeping bodies — the old, the ill, the very young,
          top-floor tenants — never get their recovery hours.
          Europe's worst heatwave summers each cost tens of
          thousands of lives.</li>
